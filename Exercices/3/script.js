function chaineDeCharactere (x){
    let chaine = "";//on crée une chaine vide
    for (let i = 0; i < x; i++) {// i=0, tant que i est inferieur à x on ajoute 1 à i 'jusqu'à arriver à 10)        
        chaine = chaine + "8";//tant que notre for tourne on fait chaine + 8
    }
    console.log(chaine);
}

chaineDeCharactere (10);


